package com.phenomenal.workshop.entity;
public class CommonCategories {
   private int interior;
   private int exterior;
   private int oils;
   private int performance;
   private int replacement;
   private int tools;
   private int tires;
   private int motocycle;
   public CommonCategories() {
	   super();
   }
public int getInterior() {
	return interior;
}
public void setInterior(int interior) {
	this.interior = interior;
}
public int getExterior() {
	return exterior;
}
public void setExterior(int exterior) {
	this.exterior = exterior;
}
public int getOils() {
	return oils;
}
public void setOils(int oils) {
	this.oils = oils;
}
public int getPerformance() {
	return performance;
}
public void setPerformance(int performance) {
	this.performance = performance;
}
public int getReplacement() {
	return replacement;
}
public void setReplacement(int replacement) {
	this.replacement = replacement;
}
public int getTools() {
	return tools;
}
public void setTools(int tools) {
	this.tools = tools;
}
public int getTires() {
	return tires;
}
public void setTires(int tires) {
	this.tires = tires;
}
public int getMotocycle() {
	return motocycle;
}
public void setMotocycle(int motocycle) {
	this.motocycle = motocycle;
}
}
